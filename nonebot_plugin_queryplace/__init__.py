from .modules.handler import *

__plugin_usage__ = """
查询卡数插件（NoneBot2）
支持机台类型计算机均人数（配置在 NoneBot 配置项中）：
- mai: 机均人数将以舞萌 DX 的机台数量进行计算
- chu: 机均人数将以中二节奏的机台数量进行计算  
- all 或留空：机均人数将以舞萌 DX 和中二节奏的机台总数进行计算

支持本地/在线机台数据切换（配置在 NoneBot 配置项中）：
- use_online_database: true 使用在线 API 获取机厅数据
- use_online_database: false 使用本地数据

指令：
- 机厅名++：增加 1 卡
- 机厅名--：减少 1 卡
- 机厅名+N：增加 N 卡
- 机厅名-N：减少 N 卡
- 机厅名 N：直接设置卡数为 N
- 机厅名=N：直接设置卡数为 N
- 机厅名几/j/J：查询该机厅卡数（包括"有几人"、"有多少人"等表达方式）
- 订阅机厅名：订阅机厅（仅限群管理员）
- 取消订阅机厅名：取消订阅机厅（仅限群管理员）
- 添加别名 机厅名 别名：为机厅添加别名（仅限群管理员）
- 删除别名 机厅名 别名：删除机厅别名（仅限群管理员）
- 添加机厅 <店名> <地址> <舞萌 DX 机台数量> <中二节奏机台数量> <简称>：添加机厅信息（仅限群管理员）
- 删除机厅 <店名>：删除机厅信息（仅限群管理员）
- j 或 几 或 jtj：查询本群所有机厅卡数
- 查找机厅 <关键词>：使用关键词查找机厅
- 机厅名 有谁：查看该机厅的历史记录
- 机厅全名/简称在哪：查询机厅地址
"""

